import lombok.NoArgsConstructor;
import lombok.Setter;

@Getter
@Setter
@JsonIgnoreProperties(ignoreUnknown = true)
@NoArgsConstructor
@AllArgsConstructor
public class Temperature {
    private double temp;
    @JsonProperty("feels_like")
    private double feelsLike;
}
